<h1>Methodology</h1>
<br />
<br />
<h2>Pre-2022 Scoring</h2>
<br />
<p>
  As is widely known, the <Link
    href="https://www.gymnastics.sport/publicdir/rules/files/en_TRA%20CoP%202022-2024.pdf"
    >2022-2024 Trampoline Gymnastics Code of Points</Link
  > introduced a number of changes. Notably, the number of execution scores contributing
  to double-mini and tumbling scoring was reduced from 3 to 2. As such, scores from
  before 2022 are not directly comparable to scores from 2022 onwards.
</p>
<br />
<p>
  Although execution scores are not currently displayed anywhere on the site
  (yet), they are used to order videos of routines when multiple are available. <u
    >To obtain a "new" (post-2022) score from a pre-2022 score, I have
    subtracted that routine's degree of difficulty, then divided the remainder
    by 3/2 to correct the number of execution scores that count.</u
  > Although this isn't perfect (namely due to some chair deductions remaining constant
  between both periods), I believe the approximation is reasonable enough for what
  I'm using it for.
</p>
<br />
<p>
  I feel that my reasoning here is worth explaining. Arbitrarily redefining
  parts of the scoring system based purely on personal opinion is an act of
  disrespect to the sport and its athletes. As an athlete and judge myself, I
  feel that modifying scores in <em>any</em> way, even for practical reasons like
  these, sets a dangerous precedent. As such, I aim to be as transparent as possible.
</p>
<br />
<br />
<h2>Video hosting and sources</h2>
<br />
<p>
  All the videos on ddcalc.net are YouTube videos embedded from one of two
  sources:
</p>
<br />
<OrderedList>
  <ListItem
    >The <Link href="https://www.youtube.com/user/figchannel"
      >FIG YouTube Channel</Link
    >: The International Gymnastics Federation (FIG) has uploaded videos of
    individual athletes' routines from various world championships.
    Frustratingly, FIG doesn't organize these videos into playlists, so it is
    likely that some videos are missing by virtue of not appearing in my search.
    This aside, routines from 2017, 2018, 2019, 2022, and 2023 World
    Championships are searchable on ddcalc.net.
  </ListItem>
  <ListItem
    >The <Link href="https://www.youtube.com/channel/UCQACl7EaWputENL5i0p9-xA"
      >ddcalc.net YouTube Channel</Link
    >: These are all videos of skills, all recorded manually. Most of these
    skills were recorded by me; thank you to Simon Neufeld for assisting with
    some of the skills as well. So far, we only have videos for double-mini, but
    we have a long-term plan to record skills on the other events as well.
  </ListItem>
</OrderedList>
<br />
<p>
  Filming all the skills takes a lot of time and effort and limits the videos on
  the site to my own level of skill and technique. So, if you have any videos of
  skills or routines that you think would make a valuable addition to the site,
  please reach out to me at <Link href="mailto:ddcalcnet@gmail.com"
    >ddcalcnet@gmail.com</Link
  >. I request that any video be hosted on your own YouTube channel to avoid any
  issues of ownership. Your videos are yours.
</p>
