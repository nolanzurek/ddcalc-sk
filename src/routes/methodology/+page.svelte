<h3>Which videos are added?</h3>
<br />
<p>
  Videos where an athlete crashes or (accidentally) breaks form considerably
  aren't generally visible in routine search results, for a few reasons:
</p>
<br />
<UnorderedList>
  <ListItem>
    My intention is to provide a good technical model for skills.
  </ListItem>
  <ListItem>
    If a routine isn't something I would want to post myself (e.g. due to a
    fall), I don't think it's fair to make it more visible by including it as a
    video result. Many routines on the FIG channel are performed by my teammates
    and friends; I feel it's important to treat each athlete with the same
    respect I would want for myself and my teammates.
  </ListItem>
</UnorderedList>
<br />
<p>
  There are a few exceptions, notably when an athlete touches their hands to the
  landing zone shortly after a double mini pass in a safe and controlled manner.
  In this case, although the original score and DD is displayed in the video,
  the routine will appear in the search results.
</p>
